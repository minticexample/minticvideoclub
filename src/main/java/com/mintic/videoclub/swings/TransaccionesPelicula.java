    public void setOperacion(String operacion) {
        this.operacion = operacion;

        if ((this.operacion.equals("alquiler"))) {
            cargarAlquilerInicial();
        } else {
            cargarDevolucionInicial();
        }
    }

    private void cargarAlquilerInicial() {
        jLTransacciones.setText("Alquilar pelicula");
        jBDevolucion.setEnabled(false);
        jBDevolucion.setVisible(false);
    }

    private void cargarDevolucionInicial() {
        jLTransacciones.setText("Devolver pelicula");
        jBAlquilar.setEnabled(false);
        jBAlquilar.setVisible(false);
        jTFIdentificacion.setEditable(false);
        jBBuscar.setEnabled(false);
        jCBPlan.setEnabled(false);
    }

